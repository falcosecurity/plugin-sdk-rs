fn bench_parse(c: &mut Criterion) {
    let mut g = c.benchmark_group("event_parse_one");
    for (i, event) in EVENTS.iter().enumerate() {
        g.throughput(Throughput::Elements(1));
        g.bench_with_input(BenchmarkId::new("parse", i), event, |b, e| {
            b.iter(|| test_parse(black_box(e)))
        });
    }

    g.finish();
}

